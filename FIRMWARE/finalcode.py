# 1. THE STABLE DRIVER
class SSD1306_Miku:
    def __init__(self, i2c):
        self.i2c = i2c
        self.addr = 0x3C

        # Initialization Sequence
        init_cmds = [
            0xAE, 0x20, 0x00, 0xD5, 0x80, 0xA8, 0x1F, 0xD3, 0x00,
            0x40, 0x8D, 0x14, 0xA1, 0xC8, 0xDA, 0x02, 0x81, 0x7F,
            0xD9, 0xF1, 0xDB, 0x40, 0xA4, 0xA6, 0xAF
        ]
        self._send_cmds(init_cmds)

    def _send_cmds(self, cmds):
        while not self.i2c.try_lock(): pass
        try:
            for cmd in cmds:
                self.i2c.writeto(self.addr, bytes([0x00, cmd]))
        finally:
            self.i2c.unlock()

    def update(self, buf):
        while not self.i2c.try_lock(): pass
        try:
            # Tell OLED to reset cursor to top-left (0,0)
            # This prevents the "Shift/Wrap" bug
            self.i2c.writeto(self.addr, b'\x00\x21\x00\x7f\x22\x00\x03')
            # Send the pixels
            self.i2c.writeto(self.addr, b'\x40' + buf)
        finally:
            self.i2c.unlock()

